 What's the difference between APY and APR?
 </button>
 </h4>
 <div id="faq1" class="accordion-collapse collapse show" data-bs-parent="#faqAccordion">
 <div class="accordion-body">
 <p class="text-success fw-bold"><i class="fas fa-exchange-alt me-2"></i>Key distinction:</p>
 <ul>
 <li><strong>APY (Annual Percentage Yield):</strong> Includes compounding effect - what you EARN on investments</li>
 <li><strong>APR (Annual Percentage Rate):</strong> Excludes compounding - what you PAY on loans</li>
 <li><strong>Example:</strong> 6% nominal rate, monthly compounding</li>
 <li><strong>APY:</strong> 6.17% (actual return you earn)</li>
 <li><strong>APR:</strong> 6.00% (stated rate without compounding)</li>
 <li><strong>For Savers:</strong> APY is higher than APR (good!)</li>
 <li><strong>For Borrowers:</strong> APR is lower than APY (misleading!)</li>
 </ul>
 </div>
 </div>
