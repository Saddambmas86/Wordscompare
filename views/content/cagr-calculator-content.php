                                <div class="example-box bg-light p-3 rounded">
                                    <strong>Example:</strong> Investment grows from ₹100 to ₹400 in 4 years
                                    <ul class="mt-2 mb-0">
                                        <li><strong>Simple Average:</strong> Total gain 300% ÷ 4 years = 75%/year ❌ (misleading!)</li>
