                                <ul>
                                    <li><strong>Problem:</strong> Investments don't grow evenly (Year 1: +20%, Year 2: -10%, Year 3: +30%)</li>
                                    <li><strong>Solution:</strong> CAGR provides single "average" annual rate that describes overall growth</li>
                                    <li><strong>Benefit:</strong> Enables apples-to-apples comparison between different investments</li>
                                    <li><strong>Example:</strong> Fund A (volatile): 15% CAGR vs Fund B (steady): 12% CAGR → Fund A performed better despite volatility</li>
