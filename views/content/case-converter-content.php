                                            <div class="card-header bg-success text-white fw-bold">
                                                Title Case (Headline Style)
                                            </div>
                                            <div class="card-body">
                                                <p class="small mb-2"><strong>Rules:</strong></p>
                                                <ul class="small mb-0">
                                                    <li>Capitalize first letter of major words</li>
                                                    <li>Lowercase articles (a, an, the)</li>
                                                    <li>Lowercase short prepositions (in, on, at, to)</li>
                                                    <li>Lowercase coordinating conjunctions (and, but, or)</li>
                                                    <li>Always capitalize first & last word</li>
                                                </ul>
                                                <p class="mt-2 mb-0"><strong>Example:</strong> "The Quick Brown Fox Jumps Over the Lazy Dog"</p>
                                            </div>
                                        </div>
                                    </div>
                                    <div class="col-md-6">
                                        <div class="card border-info border-2">
                                            <div class="card-header bg-info text-white fw-bold">
                                                Sentence Case (Running Text Style)
                                            </div>
                                            <div class="card-body">
                                                <p class="small mb-2"><strong>Rules:</strong></p>
                                                <ul class="small mb-0">
                                                    <li>Capitalize ONLY first word of sentence</li>
                                                    <li>Capitalize proper nouns (names, places)</li>
                                                    <li>All other words in lowercase</li>
                                                    <li>Used for normal paragraphs, essays</li>
                                                    <li>Standard for academic writing</li>
                                                </ul>
                                                <p class="mt-2 mb-0"><strong>Example:</strong> "The quick brown fox jumps over the lazy dog."</p>
                                            </div>
                                        </div>
                                    </div>
