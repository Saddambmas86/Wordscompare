 <div class="card border-success border-2 shadow-sm">
 <div class="card-header bg-success text-white fw-bold">
 <i class="fas fa-formula me-2"></i>The Compound Interest Formula:
 </div>
 <div class="card-body">
 <div class="text-center mb-4">
 <div class="d-inline-block bg-light border rounded px-5 py-4">
 <code class="fs-4 fw-bold text-primary">
 A = P(1 + r/n)^(nt)<br>
 <small class="text-muted d-block mt-2 fs-6">Where: A = Final Amount, P = Principal, r = Annual Rate, n = Compounds per Year, t = Years</small>
 </code>
 </div>
 </div>
 
 <div class="row g-3">
 <div class="col-md-6">
 <h6 class="fw-bold text-primary">With Regular Monthly Contributions:</h6>
 <p class="small">The formula becomes more powerful when you add monthly investments (SIP):</p>
 <code class="d-block bg-light p-3 rounded">
 Total = P(1 + r/n)^(nt) + PMT × [((1 + r/n)^(nt) - 1) / (r/n)]<br>
 <small class="text-muted">PMT = Monthly contribution amount</small>
 </code>
 </div>
 <div class="col-md-6">
 <h6 class="fw-bold text-success">Compounding Frequency Options:</h6>
 <ul class="small">
 <li><strong>Monthly (n=12):</strong> Interest calculated and added 12 times per year</li>
 <li><strong>Quarterly (n=4):</strong> Interest compounded every 3 months</li>
 <li><strong>Half-Yearly (n=2):</strong> Interest compounded twice per year</li>
 <li><strong>Annually (n=1):</strong> Interest calculated once per year</li>
 </ul>
 </div>
 </div>
