                        <li><strong class="text-primary">Interest Component:</strong> Cost of borrowing money - bank's profit</li>
                        <li><strong class="text-primary">Front-Loaded Interest:</strong> Initial EMIs have higher interest, lower principal repayment</li>
                        <li><strong class="text-primary">Gradual Shift:</strong> Over time, principal portion increases, interest decreases</li>
                    </ul>
