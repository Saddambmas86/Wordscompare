 <div class="accordion-body">
 <p class="text-success fw-bold"><i class="fas fa-calculator me-2"></i>Compound interest formula:</p>
 <ul>
 <li><strong>Principal:</strong> Your initial investment amount</li>
 <li><strong>Compounding:</strong> Interest earned on interest (quarterly/monthly/yearly)</li>
 <li><strong>Example:</strong> ₹1 Lakh @ 7% for 5 years (quarterly compounding)</li>
 <li><strong>Maturity:</strong> ₹1,41,478 (Interest earned: ₹41,478)</li>
 <li><strong>Effective Rate:</strong> 7.19% (due to quarterly compounding)</li>
 </ul>
