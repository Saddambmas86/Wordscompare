 What happens if I break FD prematurely?
 </button>
 </h4>
 <div id="faq2" class="accordion-collapse collapse" data-bs-parent="#faqAccordion">
 <div class="accordion-body">
 <p class="text-warning fw-bold"><i class="fas fa-exclamation-triangle me-2"></i>Premature withdrawal penalties:</p>
 <ul>
 <li><strong>Penalty:</strong> 0.5-1% lower interest rate than contracted rate</li>
 <li><strong>Minimum Period:</strong> Most banks allow after 7 days only</li>
 <li><strong>Revised Rate:</strong> Applicable rate for period FD remained with bank</li>
 <li><strong>Example:</strong> Broke 3-year FD after 1 year Get 1-year FD rate minus 0.5%</li>
 <li><strong>Tip:</strong> Use loan against FD instead (lower interest, FD continues earning)</li>
