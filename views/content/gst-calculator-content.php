 <i class="fas fa-layer-group me-2"></i>GST Structure - CGST, SGST, IGST:
 </div>
 <div class="card-body">
 <div class="row g-3">
 <div class="col-md-4">
 <div class="card border-primary h-100">
 <div class="card-header bg-primary text-white fw-bold">
 <i class="fas fa-building me-2"></i>Intra-State (Within State):
 </div>
 <div class="card-body">
 <p class="small"><strong>When buyer and seller are in same state:</strong></p>
 <ul class="small mb-0">
 <li><strong>CGST:</strong> Central GST (50% of total GST) - goes to Central Govt</li>
 <li><strong>SGST:</strong> State GST (50% of total GST) - goes to State Govt</li>
 <li><strong>Example:</strong> 18% GST = 9% CGST + 9% SGST</li>
 <li><strong>ITC:</strong> Both CGST and SGST credits available</li>
 </ul>
 </div>
 </div>
 </div>
 
 <div class="col-md-4">
 <div class="card border-success h-100">
 <div class="card-header bg-success text-white fw-bold">
 <i class="fas fa-map-marked-alt me-2"></i>Inter-State (Between States):
 </div>
 <div class="card-body">
 <p class="small"><strong>When buyer and seller are in different states:</strong></p>
 <ul class="small mb-0">
 <li><strong>IGST:</strong> Integrated GST (100% of total GST)</li>
 <li><strong>Collection:</strong> Collected by Centre, shared with destination state</li>
 <li><strong>Example:</strong> 18% GST = Full 18% as IGST</li>
 <li><strong>ITC:</strong> Only IGST credit available</li>
 </ul>
 </div>
 </div>
 </div>
 
 <div class="col-md-4">
 <div class="card border-warning h-100">
 <div class="card-header bg-warning text-dark fw-bold">
 <i class="fas fa-shield-alt me-2"></i>UTGST (Union Territories):
 </div>
 <div class="card-body">
 <p class="small"><strong>For Union Territories without legislature:</strong></p>
 <ul class="small mb-0">
 <li><strong>CGST + UTGST:</strong> Applies in Delhi, Puducherry, J&K, etc.</li>
 <li><strong>Structure:</strong> Similar to CGST+SGST model</li>
 <li><strong>Rate:</strong> Same as intra-state supplies</li>
 <li><strong>Example:</strong> 18% GST = 9% CGST + 9% UTGST</li>
 </ul>
 </div>
