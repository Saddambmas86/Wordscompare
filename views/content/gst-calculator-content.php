 <div class="example-box border-start border-4 border-primary ps-3 my-3">
 <h6 class="fw-bold">Complete ITC Example - Manufacturing Cycle:</h6>
 <p class="small mb-2"><strong>Stage 1 - Raw Material Supplier:</strong></p>
 <ul class="small mb-2">
 <li>Sells raw material to Manufacturer for ₹10,000 + 18% GST = ₹11,800</li>
 <li>Supplier collects ₹1,800 GST, deposits to government</li>
 </ul>
 
 <p class="small mb-2"><strong>Stage 2 - Manufacturer:</strong></p>
 <ul class="small mb-2">
 <li>Manufactures finished goods, sells to Retailer for ₹15,000 + 18% GST = ₹17,700</li>
 <li>Output GST liability: ₹2,700</li>
 <li><strong class="text-success">Less: ITC on raw material:</strong> ₹1,800</li>
 <li><strong class="text-success">Net GST to pay:</strong> ₹2,700 - ₹1,800 = ₹900 only!</li>
 </ul>
 
 <p class="small mb-2"><strong>Stage 3 - Retailer:</strong></p>
 <ul class="small mb-0">
 <li>Sells to Consumer for ₹20,000 + 18% GST = ₹23,600</li>
 <li>Output GST: ₹3,600</li>
 <li><strong class="text-success">Less: ITC on purchase:</strong> ₹2,700</li>
 <li><strong class="text-success">Net GST payable:</strong> ₹3,600 - ₹2,700 = ₹900</li>
 </ul>
 </div>
 
 <div class="alert alert-success mt-3 mb-0">
 <h6 class="alert-heading fw-bold"><i class="fas fa-hand-holding-usd me-2"></i>Key Benefit:</h6>
 <p class="mb-0 small">Without ITC, total GST collected would be ₹1,800 + ₹2,700 + ₹3,600 = ₹8,100 (cascading effect). With ITC, total GST = ₹1,800 + ₹900 + ₹900 = ₹3,600. <strong>Benefit to trade:</strong> ₹4,500 saved through ITC chain!</p>
 </div>
 </div>
 </div>
 
 <p><strong>ITC Eligibility Conditions:</strong></p>
 <ul>
 <li><strong class="text-primary">Possession of Tax Invoice:</strong> Must have valid GST invoice from registered supplier</li>
 <li><strong class="text-primary">Receipt of Goods/Services:</strong> Actual delivery must occur (not just billing)</li>
 <li><strong class="text-primary">Payment Made:</strong> Must pay supplier within 180 days (otherwise ITC reverses with interest)</li>
 <li><strong class="text-primary">GST Return Filed:</strong> Both supplier and recipient must file returns correctly</li>
 <li><strong class="text-primary">Business Use:</strong> ITC only for business purposes, not personal consumption</li>
 </ul>
