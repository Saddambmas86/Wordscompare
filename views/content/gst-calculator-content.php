 <h2 class="mb-4"><i class="fas fa-cog fa-spin me-2 text-primary"></i>How GST Calculation Works - Formulas & Examples</h2>
 
 <div class="card bg-light border-0 mb-4">
 <div class="card-body">
 <div class="alert alert-primary" role="alert">
 <h5 class="alert-heading fw-bold"><i class="fas fa-calculator me-2"></i>GST Formulas:</h5>
 <div class="row g-3 mt-3">
 <div class="col-md-6">
 <code class="fs-6 d-block bg-white p-3 rounded border">
 <strong class="text-primary">GST Exclusive (Adding GST):</strong><br>
 GST Amount = Original Price × (GST Rate ÷ 100)<br>
 Total Price = Original Price + GST Amount
 </code>
 </div>
 <div class="col-md-6">
 <code class="fs-6 d-block bg-white p-3 rounded border">
 <strong class="text-success">GST Inclusive (Removing GST):</strong><br>
 GST Amount = Total Price × [GST Rate ÷ (100 + GST Rate)]<br>
 Original Price = Total Price - GST Amount
 </code>
