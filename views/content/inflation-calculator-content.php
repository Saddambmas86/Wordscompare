 <div class="accordion-item mb-3 border">
 <h4 class="accordion-header">
 <button class="accordion-button collapsed fw-bold" type="button" data-bs-toggle="collapse" data-bs-target="#faq2">
 <span class="faq-badge me-3">Q2</span>
 How do I calculate real return on my investments?
 </button>
 </h4>
 <div id="faq2" class="accordion-collapse collapse" data-bs-parent="#faqAccordion">
 <div class="accordion-body">
 <p>Real return = What you actually earn after inflation</p>
 <div class="formula-box bg-light p-3 rounded mb-3">
 <strong>Formula:</strong> Real Return = [(1 + Nominal Return) ÷ (1 + Inflation)] - 1
 <br><small class="text-muted">Or approximately: Real Return Nominal Return - Inflation</small>
 </div>
 <h6 class="fw-bold text-primary">Examples:</h6>
 <ul>
 <li><strong>Fixed Deposit:</strong> 7% return - 6% inflation = <strong>1% real return</strong></li>
 <li><strong>Equity Mutual Fund:</strong> 12% - 6% = <strong>6% real return</strong></li>
 <li><strong>Gold:</strong> 8% - 6% = <strong>2% real return</strong></li>
 <li><strong>Real Estate:</strong> 9% - 6% = <strong>3% real return</strong></li>
 </ul>
 <div class="alert alert-warning mt-2 mb-0">
 <strong>Important:</strong> After-tax real return is even lower! FD at 7% becomes 5.25% post-tax (30% bracket), then 5.25% - 6% = <strong>-0.75% negative real return!</strong>
 </div>
 </div>
 </div>
 </div>

