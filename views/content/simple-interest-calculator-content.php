 What is the simple interest formula?
 </button>
 </h3>
 <div id="faq19-2" class="accordion-collapse collapse" data-bs-parent="#faqAccordion2">
 <div class="accordion-body">
 The simple interest formula is straightforward: <strong>SI = (P × R × T) / 100</strong>, where P = Principal amount, R = Annual interest rate (percentage), and T = Time period in years. Unlike compound interest, simple interest is calculated only on the original principal, not on accumulated interest. For example, if you invest ₹50,000 at 8% per annum for 5 years, simple interest = (50,000 × 8 × 5) / 100 = ₹20,000. Total amount received = Principal + Interest = ₹70,000. This linear calculation makes simple interest predictable and easy to understand.
