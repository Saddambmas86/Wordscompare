 <button class="accordion-button fw-bold" type="button" data-bs-toggle="collapse" data-bs-target="#faq1">
 <span class="faq-badge me-3">Q1</span>
 What is Step-Up SIP?
 </button>
 </h4>
 <div id="faq1" class="accordion-collapse collapse show" data-bs-parent="#faqAccordion">
 <div class="accordion-body">
 <p class="text-success fw-bold"><i class="fas fa-arrow-trend-up me-2"></i>Increase SIP annually:</p>
 <ul>
 <li><strong>Concept:</strong> Increase SIP amount by fixed % every year (typically 5-20%)</li>
 <li><strong>Why:</strong> As income grows, invest more without lifestyle impact</li>
 <li><strong>Impact:</strong> ₹10K SIP with 10% annual step-up for 20 years @ 15% = ₹1.18 Crore vs ₹45 Lakhs without step-up!</li>
 <li><strong>Ideal For:</strong> Young professionals expecting salary hikes</li>
 <li><strong>Implementation:</strong> Manually increase SIP or use auto step-up facility (some AMCs offer)</li>
 </ul>
 </div>
 </div>
 </div>
 
 <div class="accordion-item mb-3 border">
 <h4 class="accordion-header">
 <button class="accordion-button collapsed fw-bold" type="button" data-bs-toggle="collapse" data-bs-target="#faq2">
 <span class="faq-badge me-3">Q2</span>
 Is SIP better than lump sum investment?
 </button>
 </h4>
 <div id="faq2" class="accordion-collapse collapse" data-bs-parent="#faqAccordion">
 <div class="accordion-body">
 <p class="text-info fw-bold"><i class="fas fa-balance-scale me-2"></i>Depends on situation:</p>
 <ul>
 <li><strong>SIP Advantages:</strong> Rupee cost averaging, no market timing needed, discipline, affordable</li>
 <li><strong>Lump Sum Advantages:</strong> Full money works immediately, better in rising markets</li>
 <li><strong>Studies Show:</strong> SIP outperforms in volatile/sideways markets; Lump sum wins in bull markets</li>
 <li><strong>Hybrid Approach:</strong> Invest surplus via STP (Systematic Transfer Plan) over 6-12 months</li>
 <li><strong>For Most People:</strong> SIP is psychologically easier and financially safer</li>
 </ul>
